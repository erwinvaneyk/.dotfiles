#   extract:  Extract most know archives with one command
#   ---------------------------------------------------------
extract () {
 if [ -f $1 ] ; then
  case $1 in
    *.tar.bz2)   tar xjf $1     ;;
    *.tar.gz)    tar xzf $1     ;;
    *.bz2)       bunzip2 $1     ;;
    *.rar)       unrar e $1     ;;
    *.gz)        gunzip $1      ;;
    *.tar)       tar xf $1      ;;
    *.tbz2)      tar xjf $1     ;;
    *.tgz)       tar xzf $1     ;;
    *.zip)       unzip $1       ;;
    *.Z)         uncompress $1  ;;
    *.7z)        7z x $1        ;;
    *)     echo "'$1' cannot be extracted via extract()" ;;
     esac
 else
     echo "'$1' is not a valid file"
 fi
}

# retry function adapted from:
# https://unix.stackexchange.com/questions/82598/how-do-i-write-a-retry-logic-in-script-to-keep-retrying-to-run-it-upto-5-times/82610
function retry {
  local n=1
  local max=5
  local delay=5
  while true; do
    "$@" && break || {
      if [[ $n -lt $max ]]; then
        ((n++))
        >&2 echo "Command failed. Attempt $n/$max:"
        sleep $delay;
      else
        >&2 echo "The command has failed after $n attempts."
        exit 1;
      fi
    }
  done
}


docker_reset() {
  unset DOCKER_TLS_VERIFY
  unset DOCKER_CERT_PATH
  unset DOCKER_MACHINE_NAME
  unset DOCKER_HOST
  unset DOCKER_API_VERSION
}

docker_config() {
  env | grep DOCKER
}

docker_cleanup() {
	docker ps --filter "status=exited" | grep 'weeks ago' | awk '{print $1}' | xargs --no-run-if-empty docker rm
	docker images | grep "<none>" | awk '{print $3}' | xargs docker rmi
}

kontext() {
	NS=$1
	if [ ! -z "$NS" ] ; then
		kubectl config set-context $(kubectl config current-context) --namespace=${NS}
	fi
	kubectl config view -o jsonpath='{.contexts[0].context.namespace}'
}
